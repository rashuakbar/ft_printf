#include "libft.h"

void	*ft_bzero(void *src, size_t n)
{
	size_t	i;
	char	*s;

	s = (char *) src;
	i = 0;
	while (i < n)
	{
		s[i] = '\0';
		i++;
	}
	return ((void *)s);
}
